package Permission_modifier;

public class introduction {
    /**
        *Permission modifier  权限修饰符   即可以修饰变量也可以修饰方法
        *1.private           :只能在本包本类中访问                     即 本类
        *2.缺省               :既能在本包本类，也可以本包不同类，          即 本包
        *3.proceted          :即能在本包，本类，也可以再所有包中的子类     即 本包 + 所有子类（其他包也可以）
        *4.public            :本包，其他包都可以                       即 所有
    */
}
